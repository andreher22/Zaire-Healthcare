/** Color de urgencia */
export const getColorUrgencia = (urgencia) => {
    switch (urgencia) {
        case 'crítica': return '#B00020';
        case 'alta': return '#E65100';
        case 'moderada': return '#DDA15E';
        case 'baja': return '#4CAF50';
        default: return '#999';
    }
};

/** Etiqueta de urgencia */
export const getLabelUrgencia = (urgencia) => {
    switch (urgencia) {
        case 'crítica': return '🔴 Urgencia Crítica';
        case 'alta': return '🟠 Urgencia Alta';
        case 'moderada': return '🟡 Urgencia Moderada';
        case 'baja': return '🟢 Urgencia Baja';
        default: return '⚪ Por Determinar';
    }
};
