/** Traducir nombre de síntoma al español */
export const traducirSintoma = (sintomaEN) => {
    const key = sintomaEN.toLowerCase().replace(/ /g, '_');
    return SINTOMAS_ES[key] || sintomaEN.replace(/_/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
};

/** Traducir nombre de diagnóstico al español */
export const traducirDiagnostico = (diagEN) => {
    return DIAGNOSTICOS_ES[diagEN] || diagEN;
};

